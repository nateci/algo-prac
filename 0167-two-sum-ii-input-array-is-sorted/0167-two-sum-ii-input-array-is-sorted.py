class Solution:
    def twoSum(self, numbers: List[int], target: int) -> List[int]:
        left = 0
        right = len(numbers) - 1

        while left < right:
            goal = numbers[left] + numbers[right]

            if goal == target:
                return left + 1, right + 1

            if goal < target:
                left += 1
            else:
                right -= 1

        return -1